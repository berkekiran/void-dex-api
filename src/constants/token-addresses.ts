// Helper to get token symbol by address
export function getTokenSymbol(chainId: number, address: string): string | undefined {
  const chainTokens = TOKEN_ADDRESSES[chainId];
  if (!chainTokens) return undefined;

  const lowerAddress = address.toLowerCase();
  for (const [symbol, addr] of Object.entries(chainTokens)) {
    if (addr.toLowerCase() === lowerAddress) {
      return symbol;
    }
  }
  return undefined;
}

// Helper to get all token addresses for a chain
export function getAllTokenAddresses(chainId: number): string[] {
  const chainTokens = TOKEN_ADDRESSES[chainId];
  if (!chainTokens) return [];

  return Object.values(chainTokens).filter(
    addr => addr !== '0xEeeeeEeeeEeEeeEeEeEeeEEEeeeeEeeeeeeeEEeE'
  );
}
